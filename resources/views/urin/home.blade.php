        Biarkan selama 20 - 30 menit pada suhu ruang (10-30° C).
    </li>
    <li>Pastikan instrument (UF-4000 / UF-5000) dalam kondisi mode “Quality Control”.</li>
    <li>Kocok material uji beberapa kali hingga tidak ada partikel yang menumpuk di dasar botol, kemudian kocok kuat – kuat hingga 20 kali.</li>
    <li>Secara cepat (dalam 10 detik) setelah pengocokan, teteskan isi material uji ke dalam sampel cup sebanyak 0.6 mL (± 12 - 15 tetes).</li>
    <li>
        Secara cepat (dalam 10 detik), Letakkan cup sampel tersebut ke dalam STAT UNIT dengan bantuan adaptor, 
        lalu dorong STAT UNIT ke dalam alat dan tekan ANALYSIS START
    </li>
    <li>Hasil dapat dilihat di Monitor dan otomatis terkirim ke U-WAM</li>
    <li>Buang cup uji setelah digunakan karena tidak diperbolehkan untuk uji secara duplo dari wadah yang sama.</li>
    <li>Lakukan analisis sebanyak 6 kali berturut-turut. Laporkan hasil analisis ke 2 – 6.</li>
    <li>Simpan kembali material di lemari pendingin pada suhu 2-8° C.</li>
    <li>
        Laporkan hasil analisis parameter dalam satuan kuantitatif (/uL) dan hasil analisis conductivity dalam satuan mS/cm 
