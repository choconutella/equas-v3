<h5>Prosedur</h5>
<ol>
    <li>Tanggal pelaksanaan EQUAS periode FY 2021-02  ini adalah pada tanggal 21-22 Februari 2022. </li>
    <li>Material yang diterima disimpan di lemari pendingin pada suhu 2-8째 C.</li>
    <li>Persiapan material EQUAS dilakukan dengan mengeluarkan vial A dan B (untuk D-Dimer), dan vial C dan D (untuk PT dan APTT) dari lemari pendingin. Biarkan selama 15 menit pada suhu ruang (18-25째 C).</li>
    <li>Larutkan material A, B, C, dan D dengan akuabides masing-masing sebanyak 1.0 mL kemudian homogenisasikan perlahan untuk menghindari terjadinya busa.</li>
    <li>Diamkan material yang sudah dilarutkan selama 15 menit pada suhu ruang (18-25째 C).</li>
    <li>Homogenisasikan kembali material dengan cara membolak balikkan vial 10 kali secara perlahan. Periksa bagian dasar vial dan pastikan sampel sudah homogen (tidak ada material yang mengendap pada bagian dasar vial). </li>
    <li>Analisis material A, B, C, dan D layaknya sampel pasien sebanyak 2 kali berturut-turut untuk masing-masing material.  </li>
    <li>Simpan kembali material di lemari pendingin pada suhu 2-8째 C.</li>
    <li>Laporkan hasil dalam unit mg/L FEU dan dOD untuk D-Dimer, dan second (detik) untuk PT dan APTT pada menu MY EQUAS (mohon merujuk ke lembar Prosedur Unggah Hasil EQUAS). </li>
    <li>Jika laboratorium tidak mempunyai akses internet, pelaporan hasil dilakukan dengan mengisi dan mengirimkan formulir yang terlampir ke 0811-976-200 (Sysmex Call Center)</li>
    <li>Pelaporan hasil dilakukan <b>paling lambat hari Jumat, 25 Februari 2022</b>.  </li>
</ol>
